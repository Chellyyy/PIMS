	/**
	 * The doPost method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to post.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        response.setContentType("text/html;charset=utf-8");
        AddressDao addressDao=new AddressDao();
        Address address=new Address();
		PrintWriter out = response.getWriter();
		
		int addressid=Integer.parseInt(request.getParameter("addressid").toString());
		
		address.setAddressname(request.getParameter("addressname"));
	    address.setTelephone(request.getParameter("telephone"));
	    address.setAddress(request.getParameter("address"));
	    address.setCity(request.getParameter("city"));
	    address.setOther(request.getParameter("other"));
	    	
	    address.setAddressid(addressid);
		addressDao.UpdateAddress(address);;
		response.sendRedirect("AddressInformationServlet");
		out.flush();
		out.close();
	}

